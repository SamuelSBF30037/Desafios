public class OperadoresAritmeticos {
    public static void main(String[] args) {
System.out.println("===============================");
int numAdicao = 5 + 2;
System.out.println("Resultado da adição de 5 com 2: " + numAdicao);
int numSubtracao = 5 - 2;
System.out.println("Resultado da subtração de 5 pelo 2: " + numSubtracao);
int numMultiplicacao = 5 * 2;
System.out.println("Resultado da multiplicação de 5 pelo 2: " + numMultiplicacao);
double numDivisao = 5.0 / 2.0;
System.out.println("Resultado da divisão de 5 pelo 2: " + numDivisao);
double moduloOuResto = 5.0 % 2.0;
System.out.println("Resto da divisão de 5 pelo 2: " + moduloOuResto); System.out.println("Fim!");
System.out.println("===============================");
    }
    }
